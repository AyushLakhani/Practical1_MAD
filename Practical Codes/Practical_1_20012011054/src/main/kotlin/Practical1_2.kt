fun main()
{
    val int = 27
    println("Integer value = "+int)
    val double = int.toDouble()
    println("Double value from integer = "+double)
    val string = 45
    println("String value = "+string)
    val int1 = string.toInt()
    println("Integer value from string = "+int1)
    val int2 = string.toDouble()
    println("Double value from string = "+int2)
}